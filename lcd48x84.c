unsigned char cx = 0, cy = 0;
unsigned char lcd_frame [6][84];

static void Basic_SPI(unsigned char d)
{
	int i = 7; //MSB first.
	if(cx == 84) 
	{
		cx = 0; 
		++cy;
	}
	if(cy == 6)	cy = 0;
	LCD_GPIO->BRR = CE_PIN;
	for(; i >= 0; i--)
	{
		LCD_GPIO->BRR = CLK_PIN;
		if(d & (1 << i))
		{
			LCD_GPIO->BSRR = DIN_PIN;
		}
		else
		{
			LCD_GPIO->BRR = DIN_PIN;
		}
		LCD_GPIO->BSRR = CLK_PIN;
	}
	LCD_GPIO->BSRR = CE_PIN;
}

void Send_Data(unsigned char d)
{
	LCD_GPIO->BSRR = DC_PIN;
	Basic_SPI(d);
	lcd_frame[cy][cx++] = d;
}

static void Send_Command(unsigned char c)
{
	LCD_GPIO->BRR = DC_PIN;
	Basic_SPI(c);
}

static void Write_Char(unsigned char c)
{
	unsigned char i = 0;
	for(; i < 6; i++)
		Send_Data(font[c - 0x20][i]);
}

void Set_Address(unsigned char x, unsigned char y)
{
	Send_Command(x | 0x80);
	Send_Command(y | 0x40);
	cx = x; 
	cy = y;
}

unsigned char align(unsigned char a, unsigned char n)
{
	if(a == ALIGN_RIGHT)
		return (14 - n) * 6;
	else if(a == ALIGN_CENTER)
		return (14 - n) * 3;
	return 0;
}

void Write_String(unsigned char x, unsigned char y, char s[])
{
	Set_Address(x, y);
	while(*s)
		Write_Char(*s++);
}

void Write_Number(unsigned char x, unsigned char y, unsigned short int n)
{
	Set_Address(x, y);
	Write_Char((n / 1000) + 0x30); 			 // Binler basamagi
	Write_Char(((n / 100) % 10) + 0x30); // Yuzler basamagi
	Write_Char(((n % 100) / 10) + 0x30); // Onlar basamagi
	Write_Char((n % 10) + 0x30);				 // Birler basamagi
}

void Write_A_Pixel(unsigned char x, unsigned char y, unsigned char sr)
{
	cx = x;
	cy = y / 8;
	Send_Command(cx | 0x80);
	Send_Command(cy | 0x40);
	if(sr == PIXEL_SET)
		lcd_frame[cy][cx] |= (1 << (y % 8));
	else if(sr == PIXEL_RESET)
		lcd_frame[cy][cx] &= ~(1 << (y % 8));
	Send_Data(lcd_frame[cy][cx]);
}

void Clear_Display(void)
{
	unsigned short int i = 0;
	Set_Address(0x00, 0x00);
	for(; i < 0x01F8; i++)
		Send_Data(0x00);
}

void LCD48x84_Init(LCD48x84_InitTypedef *LCD48x84_InitStruct)
{
	LCD_GPIO->BRR = RST_PIN;
	LCD_GPIO->BSRR = RST_PIN;
	Send_Command(LCD48x84_InitStruct->Chip_Mode   | \
							 LCD48x84_InitStruct->Addressing  | \
							 Use_Extended_Instruction_Set);
	Send_Command(LCD48x84_InitStruct->Bias_System | 0x10);
	Send_Command(LCD48x84_InitStruct->Set_Vop | 0x80);
	Send_Command(LCD48x84_InitStruct->Temperature_Control);
	Send_Command(LCD48x84_InitStruct->Chip_Mode  | \
							 LCD48x84_InitStruct->Addressing | \
							 Use_Basic_Instruction_Set);	
	Send_Command(LCD48x84_InitStruct->Display_Mode);
	Clear_Display();
	Set_Address(0x00, 0x00);
}




